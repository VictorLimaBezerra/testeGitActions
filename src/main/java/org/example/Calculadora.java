package org.example;

public class Calculadora {

    public int soma(int z, int j) {
        return z + j;
    }

    public int subtracao(int z, int j) {
        return z - j;
    }

    public int multiplicacao(int z, int j) {
        return z * j;
    }

    public int divisao(int z, int j) {
        return z / j;
    }

    public boolean verificaSeEPar(int z){
        return z % 2 == 0;
    }


}